

#pegando info do usuario e convertendo para inteiro
num1 = int(input('Digite o primeiro numero'))
operador = input('Digite o operador dessejado (/ * + -)')
num2 = int(input('Digite o segundo numero'))

if operador == '+':
 print (num1 + num2)
elif operador == '-':
 print (num1 - num2)
elif operador == '/':
 print (num1 / num2)
elif operador == '*':
 print (num1 * num2)


   